const ToastItem = ({ toast, onRemove }) => (
  <div className={`pf-toast pf-toast--${toast.type}`} role="alert" aria-live="polite">
    <span className="pf-toast__icon">{ICONS[toast.type]}</span>
    <div className="pf-toast__content">
      {toast.title && <p className="pf-toast__title">{toast.title}</p>}
      <p className="pf-toast__message">{toast.message}</p>
    </div>
    <button className="pf-toast__close" onClick={() => onRemove(toast.id)} aria-label="Dismiss">
      <svg width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
        <line x1="18" y1="6" x2="6" y2="18"/>
        <line x1="6" y1="6" x2="18" y2="18"/>
      </svg>
    </button>
  </div>
);

const ToastContainer = () => {
  const { toasts, removeToast } = useToastContext();

  return (
    <div className="pf-toast-container" aria-live="polite" aria-atomic="false">
      {toasts.map((t) => (
        <ToastItem key={t.id} toast={t} onRemove={removeToast} />
      ))}
    </div>
  );
};

export default ToastContainer;
